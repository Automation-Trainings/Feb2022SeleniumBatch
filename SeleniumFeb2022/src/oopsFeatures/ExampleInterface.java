package oopsFeatures;

public interface ExampleInterface {
	
	/*
	 * Difference between Interface and Abstract
	 * Abstract can have both abstract and non abstract methods
	 * abstract can not have multiple inheritance
	 * 
	 * Interface can only have abstract methods
	 * It will support multiple inheritance
	 */
	
	void exMethod();
	void exMethod1();
	void exMethod2();
	
}
